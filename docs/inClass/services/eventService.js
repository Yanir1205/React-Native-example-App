import { saveToStorage, getFromStorage } from './storageService'

export default { query, saveEvent, getEventById }

let events = [];

function getEventById(id) {
    return Promise.resolve(events.find(item => item.id === id))

}
function saveEvent(id, title, description) {
    const newItem = { id: id || new Date(), title, description }
    if (!id) {
        events = [newItem, ...events]
    }
    else {
        events = events.map(item => item.id === id ? newItem : item)
    }
    saveToStorage('events', events)
    return Promise.resolve(newItem)

}
